var mongoose = require('mongoose')
var BlogSchema = new mongoose.Schema({
    title: String,
    summary: String,
    tags: Array,
    // date: { type: Date, default: Date.now() },
    meta: {
        createAt: {
            type: Date,
            default: Date.now()
        },
        updateAt: {
            type: Date,
            default: Date.now()
        }
    },
    category:String,
    content: String
})

BlogSchema.pre('save', function(next) {
    if (this.isNew) {
        this.meta.createAt = this.meta.updateAt = Date.now()
    } else {
        this.meta.updateAt = Date.now()
    }
    //that is the point
    next()
})

BlogSchema.statics = {
    fetch: function(cb) {
        return this
            .find({})
            .sort('meta.updateAt')
            .exec(cb)
    },
    findById: function(id, cb) {
        return this
            .findOne({ _id: id })
            .exec(cb)
    }
}

module.exports = BlogSchema
